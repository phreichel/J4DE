//*************************************************************************************************
package j4de.net;
//*************************************************************************************************

//*************************************************************************************************
import java.util.UUID;
import java.nio.ByteBuffer;
//*************************************************************************************************

//*************************************************************************************************
public abstract class NetMsg {

	//==============================================================================================
	protected UUID uuid = null;
	//==============================================================================================
	
	//==============================================================================================
	public UUID getID() {
		return this.uuid;
	}
	//==============================================================================================

	//==============================================================================================
	public abstract void marshal(ByteBuffer buffer);
	public abstract void unmarshal(ByteBuffer buffer);
	//==============================================================================================
	
}
//*************************************************************************************************
