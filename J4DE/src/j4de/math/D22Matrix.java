//*************************************************************************************************
package j4de.math;
//*************************************************************************************************


//*************************************************************************************************
public class D22Matrix {

	//==============================================================================================
	public double c11, c12;
	public double c21, c22;
	//==============================================================================================

	//==============================================================================================
	public D22Matrix() {
		super();
	}
	//==============================================================================================

	//==============================================================================================
	public D22Matrix(D22Matrix m) {
		super();
		this.set(m);
	}
	//==============================================================================================

	//==============================================================================================
	public D22Matrix(
			double c11, double c12,
			double c21, double c22) {
		this.set(
			c11, c12,
			c21, c22);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void set(D22Matrix m) {
		this.set(
			m.c11, m.c12,
			m.c21, m.c22);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void set(
			double c11, double c12,
			double c21, double c22) {
		this.c11 = c11; this.c12 = c12;
		this.c21 = c21; this.c22 = c22;
	}
	//==============================================================================================

	//==============================================================================================
	public void setZero() {
		this.set(
			0, 0,
			0, 0);
	}
	//==============================================================================================

	//==============================================================================================
	public void setIdentity() {
		this.set(
			1, 0,
			0, 1);
	}
	//==============================================================================================

	//==============================================================================================
	public double det() {
		return
			this.c11 * this.c22 -
			this.c21 * this.c12;
	}
	//==============================================================================================
	
	//==============================================================================================
	public void mul(			
			D22Matrix m,
			double s) {
		this.set(
			(m.c11*s), (m.c12*s),
			(m.c21*s), (m.c22*s));
	}
	//==============================================================================================

	//==============================================================================================
	public void div(			
			D22Matrix m,
			double d) {
		double dInv = 1.0/d;
		this.mul(m, dInv);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void add(
			D22Matrix a,
			D22Matrix b) {
		this.set(
			(a.c11+b.c11), (a.c12+b.c12),
			(a.c21+b.c21), (a.c22+b.c22));
	}
	//==============================================================================================

	//==============================================================================================
	public void sub(
			D22Matrix a,
			D22Matrix b) {
		this.set(
			(a.c11-b.c11), (a.c12-b.c12),
			(a.c21-b.c21), (a.c22-b.c22));
	}
	//==============================================================================================

	//==============================================================================================
	public void transpose(D22Matrix m) {
		this.set(
			m.c11, m.c21,
			m.c12, m.c22);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void mul(
			D22Matrix a,
			D22Matrix b) {
		this.set(
			(a.c11*b.c11) + (a.c12*b.c21),
			(a.c11*b.c12) + (a.c12*b.c22),
			(a.c21*b.c11) + (a.c22*b.c21),
			(a.c21*b.c12) + (a.c22*b.c22));
	}
	//==============================================================================================

	//==============================================================================================
	public void mulTa(
			D22Matrix a,
			D22Matrix b) {
		this.set(
			(a.c11*b.c11) + (a.c21*b.c21),
			(a.c11*b.c12) + (a.c21*b.c22),
			(a.c12*b.c11) + (a.c22*b.c21),
			(a.c12*b.c12) + (a.c22*b.c22));
	}
	//==============================================================================================

	//==============================================================================================
	public void mulTb(
			D22Matrix a,
			D22Matrix b) {
		this.set(
			(a.c11*b.c11) + (a.c12*b.c12),
			(a.c11*b.c21) + (a.c12*b.c22),
			(a.c21*b.c11) + (a.c22*b.c12),
			(a.c21*b.c21) + (a.c22*b.c22));
	}
	//==============================================================================================

	//==============================================================================================
	public void mulTaTb(
			D22Matrix a,
			D22Matrix b) {
		this.set(
			(a.c11*b.c11) + (a.c12*b.c21),
			(a.c11*b.c12) + (a.c12*b.c22),
			(a.c21*b.c11) + (a.c22*b.c21),
			(a.c21*b.c12) + (a.c22*b.c22));
	}
	//==============================================================================================

}
//*************************************************************************************************
