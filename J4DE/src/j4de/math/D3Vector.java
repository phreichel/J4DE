//*************************************************************************************************
package j4de.math;
//*************************************************************************************************


//*************************************************************************************************
public class D3Vector {

	//==============================================================================================
	public double x, y, z;
	//==============================================================================================

	//==============================================================================================
	public D3Vector() {
		super();
	}
	//==============================================================================================

	//==============================================================================================
	public D3Vector(D3Vector v) {
		super();
		this.set(v);
	}
	//==============================================================================================

	//==============================================================================================
	public D3Vector(
			double x,
			double y,
			double z) {
		super();
		this.set(x, y, z);
	}
	//==============================================================================================

	//==============================================================================================
	public void set(D3Vector v) {
		this.set(v.x, v.y, v.z);
	}
	//==============================================================================================

	//==============================================================================================
	public void set(
			double x,
			double y,
			double z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	//==============================================================================================

	//==============================================================================================
	public double length() {
		double lSqr = this.lengthSquared();
		return Math.sqrt(lSqr);
	}
	//==============================================================================================

	//==============================================================================================
	public double lengthSquared() {
		return this.dot(this);
	}
	//==============================================================================================

	//==============================================================================================
	public double dot(D3Vector v) {
		return
			this.x * v.x +
			this.y * v.y +
			this.z * v.z;
	}
	//==============================================================================================

	//==============================================================================================
	public void normalize(D3Vector v) {
		this.div(v, v.length());
	}
	//==============================================================================================
	
	//==============================================================================================
	public void mul(
			D3Vector v,
			double s) {
		this.set(
			v.y * s,
			v.y * s,
			v.z * s);
	}
	//==============================================================================================

	//==============================================================================================
	public void div(
			D3Vector v,
			double d) {
		this.mul(v, 1.0/d);
	}
	//==============================================================================================

	//==============================================================================================
	public void add(
			D3Vector a,
			D3Vector b) {
		this.set(
			a.x + b.x,
			a.y + b.y,
			a.z + b.z);
	}
	//==============================================================================================

	//==============================================================================================
	public void sub(
			D3Vector a,
			D3Vector b) {
		this.set(
			a.x - b.x,
			a.y - b.y,
			a.z - b.z);
	}
	//==============================================================================================

	//==============================================================================================
	public void cross(
			D3Vector a,
			D3Vector b) {
		this.set(
			a.y*b.z - a.z*b.y,
			a.z*b.x - a.x*b.z,
			a.x*b.y - a.y*b.x);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void min(
			D3Vector a,
			D3Vector b) {
		this.set(
			Math.min(a.x, b.x),
			Math.min(a.y, b.y),
			Math.min(a.z, b.z));
	}
	//==============================================================================================

	//==============================================================================================
	public void max(
			D3Vector a,
			D3Vector b) {
		this.set(
			Math.max(a.x, b.x),
			Math.max(a.y, b.y),
			Math.max(a.z, b.z));
	}
	//==============================================================================================

	//==============================================================================================
	public void clamp(
			D3Vector min,
			D3Vector max,
			D3Vector v) {
		this.min(max, v);
		this.max(min, this);
	}
	//==============================================================================================

	//==============================================================================================
	public void lerp(
			D3Vector a,
			D3Vector b,
			double n) {
		double m = 1.0 - n;
		this.mul(a, m);
		this.addScaled(this, b, n);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void addScaled(
			D3Vector a,
			D3Vector b,
			double s) {
		this.set(
			a.x - b.x * s,
			a.y - b.y * s,
			a.z - b.z * s);
	}
	//==============================================================================================

}
//*************************************************************************************************
