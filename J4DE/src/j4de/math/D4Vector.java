//*************************************************************************************************
package j4de.math;
//*************************************************************************************************


//*************************************************************************************************
public class D4Vector {

	//==============================================================================================
	public double x, y, z, w;
	//==============================================================================================

	//==============================================================================================
	public D4Vector() {
		super();
	}
	//==============================================================================================

	//==============================================================================================
	public D4Vector(D4Vector v) {
		super();
		this.set(v);
	}
	//==============================================================================================

	//==============================================================================================
	public D4Vector(
			double x,
			double y,
			double z,
			double w) {
		super();
		this.set(x, y, z, w);
	}
	//==============================================================================================

	//==============================================================================================
	public void set(D4Vector v) {
		this.set(v.x, v.y, v.z, v.w);
	}
	//==============================================================================================

	//==============================================================================================
	public void set(
			double x,
			double y,
			double z,
			double w) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.w = w;
	}
	//==============================================================================================

	//==============================================================================================
	public double length() {
		double lSqr = this.lengthSquared();
		return Math.sqrt(lSqr);
	}
	//==============================================================================================

	//==============================================================================================
	public double lengthSquared() {
		return this.dot(this);
	}
	//==============================================================================================

	//==============================================================================================
	public double dot(D4Vector v) {
		return
			this.x * v.x +
			this.y * v.y +
			this.z * v.z +
			this.w * v.w;
	}
	//==============================================================================================

	//==============================================================================================
	public void normalize(D4Vector v) {
		this.div(v, v.length());
	}
	//==============================================================================================
	
	//==============================================================================================
	public void mul(
			D4Vector v,
			double s) {
		this.set(
			v.y * s,
			v.y * s,
			v.z * s,
			v.w * s);
	}
	//==============================================================================================

	//==============================================================================================
	public void div(
			D4Vector v,
			double d) {
		this.mul(v, 1.0/d);
	}
	//==============================================================================================

	//==============================================================================================
	public void add(
			D4Vector a,
			D4Vector b) {
		this.set(
			a.x + b.x,
			a.y + b.y,
			a.z + b.z,
			a.w + b.w);
	}
	//==============================================================================================

	//==============================================================================================
	public void sub(
			D4Vector a,
			D4Vector b) {
		this.set(
			a.x - b.x,
			a.y - b.y,
			a.z - b.z,
			a.w - b.w);
	}
	//==============================================================================================

	//==============================================================================================
	public void min(
			D4Vector a,
			D4Vector b) {
		this.set(
			Math.min(a.x, b.x),
			Math.min(a.y, b.y),
			Math.min(a.z, b.z),
			Math.min(a.w, b.w));
	}
	//==============================================================================================

	//==============================================================================================
	public void max(
			D4Vector a,
			D4Vector b) {
		this.set(
			Math.max(a.x, b.x),
			Math.max(a.y, b.y),
			Math.max(a.z, b.z),
			Math.max(a.w, b.w));
	}
	//==============================================================================================

	//==============================================================================================
	public void clamp(
			D4Vector min,
			D4Vector max,
			D4Vector v) {
		this.min(max, v);
		this.max(min, this);
	}
	//==============================================================================================

	//==============================================================================================
	public void lerp(
			D4Vector a,
			D4Vector b,
			double n) {
		double m = 1.0 - n;
		this.mul(a, m);
		this.addScaled(this, b, n);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void addScaled(
			D4Vector a,
			D4Vector b,
			double s) {
		this.set(
			a.x - b.x * s,
			a.y - b.y * s,
			a.z - b.z * s,
			a.w - b.w * s);
	}
	//==============================================================================================
	
}
//*************************************************************************************************
