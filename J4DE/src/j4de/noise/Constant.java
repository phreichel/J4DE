//**************************************************************************************************
public class Constant extends Module {

	//==============================================================================================
	private double value = 0.0;
	//==============================================================================================
	
	//==============================================================================================
	public double getValue() {
		return this.value;
	}
	//==============================================================================================
	
	//==============================================================================================
	public void setValue(double value) {
		this.value = value;
	}
	//==============================================================================================
	
	//==============================================================================================
	protected double getCalculatedValue(double x, double y, double z) {
		return this.value;
	}
	//==============================================================================================
	
}
