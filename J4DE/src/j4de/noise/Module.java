//**************************************************************************************************
package j4de.noise;
//**************************************************************************************************

//**************************************************************************************************
public abstract class Module {

	//==============================================================================================
	private double cx = Double.NaN;
	private double cy = Double.NaN;
	private double cz = Double.NaN;
	private double cv = Double.NaN;
	//==============================================================================================

	//==============================================================================================
	public double getValue(double x, double y, double z) {
		if ((x != cx) || (y != cy) || (z != cz)) {
			cx = x;
			cy = y;
			cz = z;
			cv = this.getCalculatedValue(x, y, z);
		}
		return cv;
	}
	//==============================================================================================

	//==============================================================================================
	protected abstract double getCalculatedValue(double x, double y, double z);
	//==============================================================================================
	
}
//**************************************************************************************************
