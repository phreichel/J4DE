package j4de.noise;
//**************************************************************************************************

//**************************************************************************************************
public class Blend extends Module {

	//==============================================================================================
	private Module inputA;
	private Module inputB;
	private Module blend;
	//==============================================================================================
	
	//==============================================================================================
	public Module getInputA() {
		return this.inputA;
	}
	//==============================================================================================
	
	//==============================================================================================
	public void setInputA(Module input) {
		this.inputA = input;
	}
	//==============================================================================================

	//==============================================================================================
	public Module getInputB() {
		return this.inputB;
	}
	//==============================================================================================
	
	//==============================================================================================
	public void setInputB(Module input) {
		this.inputB = input;
	}
	//==============================================================================================

	//==============================================================================================
	public Module getBlend() {
		return this.blend;
	}
	//==============================================================================================
	
	//==============================================================================================
	public void setBlend(Module input) {
		this.blend = input;
	}
	//==============================================================================================
	
	//==============================================================================================
	protected double getCalculatedValue(double x, double y, double z) {
		double inputValueA = this.inputA.getValue(x, y, z);
		double inputValueB = this.inputB.getValue(x, y, z);
		double alpha       = (this.inputB.getValue(x, y, z) + 1.0) / 2.0;
		return NoiseGen.linearInterp(inputValueA, inputValueB, alpha);
	}
	//==============================================================================================
	
}
