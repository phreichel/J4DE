package j4de.model;
//*************************************************************************************************

//*************************************************************************************************
import java.util.UUID;
//*************************************************************************************************

//*************************************************************************************************
import j4de.geometry.Sphere;
import j4de.math.D33Matrix;
import j4de.math.D3Vector;
//*************************************************************************************************

//*************************************************************************************************
public class Body {

	//==============================================================================================
	private UUID uuid = null;
	//==============================================================================================

	//==============================================================================================
	private D3Vector position = null;
	private D3Vector velocity = null;
	//==============================================================================================

	//==============================================================================================
	private D33Matrix orientation = null;
	private D3Vector  rotation = null;
	//==============================================================================================
	
	//==============================================================================================
	public UUID getUUID() {
		return this.uuid;
	}
	//==============================================================================================

	//==============================================================================================
	public void setUUID(UUID uuid) {
		this.uuid = uuid;
	}
	//==============================================================================================
	
	//==============================================================================================
	public D3Vector getPosition() {
		return this.position;
	}
	//==============================================================================================

	//==============================================================================================
	public void setPosition(D3Vector position) {
		this.position = position;
	}
	//==============================================================================================

	//==============================================================================================
	public D3Vector getVelocity() {
		return this.velocity;
	}
	//==============================================================================================

	//==============================================================================================
	public void setVelocity(D3Vector velocity) {
		this.velocity = velocity;
	}
	//==============================================================================================

	//==============================================================================================
	public D33Matrix getOrientation() {
		return this.orientation;
	}
	//==============================================================================================

	//==============================================================================================
	public void setRotation(D33Matrix orientation) {
		this.orientation = orientation;
	}
	//==============================================================================================
	
	//==============================================================================================
	public D3Vector getRotation() {
		return this.rotation;
	}
	//==============================================================================================

	//==============================================================================================
	public void setRotation(D3Vector rotation) {
		this.rotation = rotation;
	}
	//==============================================================================================

	//==============================================================================================
	public void calculateBoundarySphere(Sphere sphere) {
		sphere.getCenter().set(this.getPosition());
		sphere.setRadius(1.0);
	}
	//==============================================================================================

	//==============================================================================================
	public void calculateBoundaryCapsule(Sphere sphere) {
		sphere.getCenter().set(this.getPosition());
		sphere.setRadius(1.0);
	}
	//==============================================================================================
	
	//==============================================================================================
	public void calculateFutureState(Body body, double time) {
		body.getPosition().addScaled(this.getPosition(), this.getVelocity(), time);
	}
	//==============================================================================================
	
}
//*************************************************************************************************
