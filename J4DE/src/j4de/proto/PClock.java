//*************************************************************************************************
package j4de.proto;
//*************************************************************************************************

//*************************************************************************************************
public class PClock {

	//==============================================================================================
	private long time  = -1L;
	private long delta = -1L;
	//==============================================================================================
	
	//==============================================================================================
	public PClock() {
		super();
		this.time  = -1L;
		this.delta = -1L;
	}
	//==============================================================================================

	//==============================================================================================
	public void init() {
		this.time  = System.currentTimeMillis();
		this.delta = 0L;
	}
	//==============================================================================================

	//==============================================================================================
	public void step() {
		long current = System.currentTimeMillis();
		this.delta   = current - this.time;
		this.time    = current;
	}
	//==============================================================================================
	
	//==============================================================================================
	public void done() {
		this.time  = -1L;
		this.delta = -1L;
	}
	//==============================================================================================

	//==============================================================================================
	public long getTime() {
		return this.time;
	}
	//==============================================================================================

	//==============================================================================================
	public long getDelta() {
		return this.delta;
	}
	//==============================================================================================
	
}
//*************************************************************************************************
