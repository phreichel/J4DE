//*************************************************************************************************
package j4de.proto;
//*************************************************************************************************

//*************************************************************************************************
public class PMain {

	//==============================================================================================
	private PServer server = null;
	private PClient client = null;
	//==============================================================================================

	//==============================================================================================
	public PMain() {
		super();
		this.server = new PServer();
		this.client = new PClient();
	}
	//==============================================================================================

	//==============================================================================================
	public void run() {
		this.server.begin();
		this.client.begin();
		boolean joined = false;
		while (!joined) {
			try {
				this.client.join();
				joined = true;
			} catch (InterruptedException e) {}
		}
		this.server.end();
		joined = false;
		while (!joined) {
			try {
				this.server.join();
				joined = true;
			} catch (InterruptedException e) {}
		}
	}
	//==============================================================================================
	
	//==============================================================================================
	public static void main(String[] args) {
		PMain main = new PMain();
		main.run();
	}
	//==============================================================================================

}
